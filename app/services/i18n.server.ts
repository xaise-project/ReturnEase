export function getTranslations(locale: string): Record<string, string> {
  return translations[locale] || translations["en"];
}

export function detectLocale(request: Request): string {
  const url = new URL(request.url);
  const langParam = url.searchParams.get("lang");
  if (langParam && translations[langParam]) return langParam;

  const acceptLang = request.headers.get("Accept-Language") || "";
  if (acceptLang.startsWith("tr")) return "tr";
  return "en";
}

export function t(translations: Record<string, string>, key: string, vars?: Record<string, string | number>): string {
  let text = translations[key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}
